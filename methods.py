#! /usr/bin/env python


class Methods:

    def pow(self, other):
        return self ** other

    def mul(self, other):
        return self * other

    def div(self, other):
        return self / other

    def f_div(self, other):
        return self // other

    def mod(self, other):
        return self % other

    def add(self, other):
        return self + other

    def sub(self, other):
        return self - other

    def lt(self, other):
        return self < other

    def le(self, other):
        return self <= other

    def eq(self, other):
        return self == other

    def ne(self, other):
        return self != other

    def ge(self, other):
        return self >= other

    def gt(self, other):
        return self > other
