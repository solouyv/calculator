#! /usr/bin/env python

class Methods:

    def pow(self, other):
        return self ** other

    def mul(self, other):
        return self * other

    def div(self, other):
        return self / other

    def f_div(self, other):
        return self // other

    def mod(self, other):
        return self % other

    def add(self, other):
        return self + other

    def sub(self, other):
        return self - other
